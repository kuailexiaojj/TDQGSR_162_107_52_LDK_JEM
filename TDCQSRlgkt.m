
function x=TDCQSRlgkt(a3,b3,c3,h1,c,e,h,x1)
x=zeros(1,length(x1));
y=zeros(1,length(x1));
x(1)=0.01;
y(1)=0.01;
for i=1:length(x1)-1
        if abs(x(i))>10
        x(i)=sign(x(i))*10;
        end
    if abs(y(i))>10
        y(i)=sign(x(i))*10;
    end
   
    k1=h*((h1*x(i))-h1*((a3.*b3+b3.*c3+c3.*a3)./(a3.*b3.*c3)).*x(i).^3+h1*((a3+b3+c3)./(a3.*b3.*c3)).*x(i).^5-(1/(a3.*b3.*c3)).*x(i).^7+c.*(y(i)-x(i))+x1(i));
    f1=h*(-e*y(i)-c.*(y(i)-x(i)));
    k2=h*((h1*(x(i)+k1/2))-h1*((a3.*b3+b3.*c3+c3.*a3)./(a3.*b3.*c3)).*(x(i)+k1/2).^3+h1*((a3+b3+c3)./(a3.*b3.*c3)).*(x(i)+k1/2).^5-(1/(a3.*b3.*c3)).*(x(i)+k1/2).^7+c.*((y(i)+f1/2)-(x(i)+k1/2))+x1(i));
    f2=h*(-e*(y(i)+f1/2)-c.*((y(i)+f1/2)-(x(i)+k1/2)));
    k3=h*((h1*(x(i)+k2/2))-h1*((a3.*b3+b3.*c3+c3.*a3)./(a3.*b3.*c3)).*(x(i)+k2/2).^3+h1*((a3+b3+c3)./(a3.*b3.*c3)).*(x(i)+k2/2).^5-(1/(a3.*b3.*c3)).*(x(i)+k2/2).^7+c.*((y(i)+f2/2)-(x(i)+k2/2))+x1(i+1));
    f3=h*(-e*(y(i)+f2/2)-c.*((y(i)+f2/2)-(x(i)+k2/2)));
    k4=h*((h1*(x(i)+k3))-h1*((a3.*b3+b3.*c3+c3.*a3)./(a3.*b3.*c3)).*(x(i)+k3).^3+h1*((a3+b3+c3)./(a3.*b3.*c3)).*(x(i)+k3).^5-(1/(a3.*b3.*c3)).*(x(i)+k3).^7+c.*((y(i)+f3)-(x(i)+k3))+x1(i+1));
    f4=h*(-e*(y(i)+f3)-c.*((y(i)+f3)-(x(i)+k3)));
    x(i+1)=x(i)+(1/6)*(k1+2*k2+2*k3+k4);
    y(i+1)=y(i)+(1/6)*(f1+2*f2+2*f3+f4); 
end
end
